def load_wmn_data() -> list[dict[str, Any]]:
    """Load the built-in wmn-data.json sites."""
    if not _WMN_DATA.exists():
        return []
    with open(_WMN_DATA, encoding="utf-8") as f:
        data = json.load(f)
    sites = data.get("sites", [])
    # Tag the source
    for s in sites:
        s.setdefault("_source", "wmn")
    return sites


def load_extended_data() -> list[dict[str, Any]]:
    """Load the extended_sites.json generated by generate_sites.py."""
    if not _EXTENDED_DATA.exists():
        return []
    with open(_EXTENDED_DATA, encoding="utf-8") as f:
        data = json.load(f)
    sites = data.get("sites", []) if isinstance(data, dict) else data
    for s in sites:
        s.setdefault("_source", "extended")
    return sites


def load_cached_remote(name: str) -> list[dict[str, Any]]:
    """Load cached remote data (Sherlock or Maigret)."""
    cache_file = _CACHE_DIR / f"{name}.json"
    if not cache_file.exists():
        return []
    with open(cache_file, encoding="utf-8") as f:
        data = json.load(f)
    return data


