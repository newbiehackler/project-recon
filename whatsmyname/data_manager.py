# Engine-based URL patterns for Maigret sites that lack explicit URLs
_ENGINE_URL_PATTERNS: dict[str, str] = {
    "XenForo": "{urlMain}/members/{username}.1/",
    "phpBB": "{urlMain}/memberlist.php?mode=viewprofile&un={username}",
    "vBulletin": "{urlMain}/member.php?username={username}",
    "Discourse": "{urlMain}/u/{username}",
    "uCoz": "{urlMain}/{username}/",
    "Flarum": "{urlMain}/u/{username}",
    "Wordpress/Author": "{urlMain}/author/{username}/",
}


def _convert_maigret(data: dict[str, Any]) -> list[dict[str, Any]]:
    """Convert Maigret data.json format to WMN site entries."""
    sites: list[dict[str, Any]] = []

    # Maigret nests sites under "sites" key
    site_list = data.get("sites", data) if isinstance(data, dict) else {}
    if not isinstance(site_list, dict):
        return sites

    for name, info in site_list.items():
        if not isinstance(info, dict):
            continue

        url_template = info.get("url", "")
        url_main = info.get("urlMain", "")

        # If no explicit URL, try to construct from engine + urlMain
        if not url_template and url_main:
            engine = info.get("engine", "")
            pattern = _ENGINE_URL_PATTERNS.get(engine, "")
            if pattern:
                url_template = pattern.replace("{urlMain}", url_main.rstrip("/"))
            else:
                # Skip sites with no URL pattern
                continue

        if not url_template:
            continue

        uri_check = url_template.replace("{username}", "{account}").replace("{}", "{account}")
        if "{account}" not in uri_check:
            continue

        check_type = info.get("checkType", info.get("errorType", "status_code"))
        error_msg = info.get("errorMsg", info.get("absenceStrs", ""))

        if check_type == "status_code":
            e_code = 200
            m_code = 404
            e_string = ""
            m_string = ""
        elif check_type == "message":
            e_code = 200
            m_code = 200
            e_string = ""
            if isinstance(error_msg, list):
                m_string = error_msg[0] if error_msg else ""
            else:
                m_string = str(error_msg) if error_msg else ""
        else:
            e_code = 200
            m_code = 404
            e_string = ""
            m_string = ""

        # Determine category from tags
        tags = info.get("tags", [])
        cat = _maigret_tags_to_cat(tags) if tags else _guess_category(name)

        entry: dict[str, Any] = {
            "name": name,
            "uri_check": uri_check,
            "e_code": e_code,
            "e_string": e_string,
            "m_string": m_string,
            "m_code": m_code,
            "known": [],
            "cat": cat,
            "_source": "maigret",
        }

        claimed = info.get("username_claimed", info.get("usernameClaimed", ""))
        if claimed:
            entry["known"] = [claimed]

        if info.get("isNSFW") or "nsfw" in tags:
            entry["cat"] = "xx NSFW xx"

        sites.append(entry)
    return sites


def _maigret_tags_to_cat(tags: list[str]) -> str:
    """Map Maigret tags to WMN categories."""
    tag_set = {t.lower() for t in tags}
    mapping = {
        "social": "social",
        "dating": "dating",
        "gaming": "gaming",
        "coding": "coding",
        "music": "music",
        "art": "art",
        "photo": "images",
        "video": "video",
        "blog": "blog",
        "finance": "finance",
        "shopping": "shopping",
        "news": "news",
        "tech": "tech",
        "health": "health",
        "hobby": "hobby",
        "business": "business",
        "political": "political",
        "search": "search",
    }
    for tag, cat in mapping.items():
        if tag in tag_set:
            return cat
    return "misc"


