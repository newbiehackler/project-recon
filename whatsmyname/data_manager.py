_PKG_DIR = Path(__file__).resolve().parent
_REPO_ROOT = _PKG_DIR.parent
_WMN_DATA = _REPO_ROOT / "wmn-data.json"
_EXTENDED_DATA = _PKG_DIR / "data" / "extended_sites.json"
_CACHE_DIR = Path.home() / ".cache" / "whatsmyname"

# Remote data source URLs (raw GitHub)
SHERLOCK_DATA_URL = (
    "https://raw.githubusercontent.com/sherlock-project/sherlock/"
    "master/sherlock_project/resources/data.json"
)
MAIGRET_DATA_URL = (
    "https://raw.githubusercontent.com/soxoj/maigret/"
    "main/maigret/resources/data.json"
)


def _ensure_cache_dir() -> Path:
    _CACHE_DIR.mkdir(parents=True, exist_ok=True)
    return _CACHE_DIR


