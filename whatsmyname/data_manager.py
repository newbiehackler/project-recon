def _domain_key(uri: str) -> str:
    """Extract domain from a URI for dedup purposes."""
    try:
        parsed = urlparse(uri)
        return parsed.netloc.lower().removeprefix("www.")
    except Exception:
        return uri.lower()


def merge_and_dedup(
    *site_lists: list[dict[str, Any]],
) -> list[dict[str, Any]]:
    """Merge multiple site lists and deduplicate by domain + path pattern.

    Priority: wmn > extended > sherlock > maigret
    """
    source_priority = {"wmn": 0, "extended": 1, "sherlock": 2, "maigret": 3}
    seen: dict[str, dict[str, Any]] = {}

    for sites in site_lists:
        for site in sites:
            uri = site.get("uri_check", "")
            if not uri:
                continue
            key = _domain_key(uri) + urlparse(uri).path.split("{")[0]
            source = site.get("_source", "unknown")
            priority = source_priority.get(source, 99)

            if key not in seen or priority < source_priority.get(
                seen[key].get("_source", "unknown"), 99
            ):
                seen[key] = site

    result = sorted(seen.values(), key=lambda s: s.get("name", "").lower())
    return result


def get_all_categories(sites: list[dict[str, Any]]) -> list[str]:
    """Return sorted list of all categories present in the sites."""
    cats = {s.get("cat", "misc") for s in sites}
    return sorted(cats)


