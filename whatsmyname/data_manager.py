def load_all_local() -> list[dict[str, Any]]:
    """Load WMN + extended + cached remote data (no network)."""
    wmn = load_wmn_data()
    extended = load_extended_data()
    sherlock_cached = load_cached_remote("sherlock")
    maigret_cached = load_cached_remote("maigret")
    return merge_and_dedup(wmn, extended, sherlock_cached, maigret_cached)


async def load_all_with_update() -> tuple[list[dict[str, Any]], dict[str, int]]:
    """Load all data including fresh remote fetch. Returns (sites, stats)."""
    wmn = load_wmn_data()
    extended = load_extended_data()
    sherlock = await fetch_and_cache_sherlock()
    maigret = await fetch_and_cache_maigret()

    merged = merge_and_dedup(wmn, extended, sherlock, maigret)

    stats = {
        "wmn": len(wmn),
        "extended": len(extended),
        "sherlock": len(sherlock),
        "maigret": len(maigret),
        "total_merged": len(merged),
    }
    return merged, stats
